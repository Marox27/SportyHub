package com.example.sportyhub.Modelos;

public class AjusteItem {
    private int icono;
    private String titulo;

    public AjusteItem(int icono, String titulo) {
        this.icono = icono;
        this.titulo = titulo;
    }

    public int getIcono() {
        return icono;
    }

    public String getTitulo() {
        return titulo;
    }
}

